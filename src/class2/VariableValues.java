package class2;

public class VariableValues {
    public static void main(String[] args) {
        byte bnum=122; bnum=-122;
        short snum=2000; snum=2022;
        int nnum=123456789; nnum=156743;
        double dnum=54.7; dnum=12.2;
        float fnum=546.5f; fnum=7643.8f;
        long lnum=100000000; lnum=110000000;
        char c='H'; c='@';
        boolean b=false; b=true;
        System.out.println("bnum="+bnum);
        System.out.println("snum="+snum);
        System.out.println("nnum="+nnum);
        System.out.println("dnum="+dnum);
        System.out.println("fnum="+fnum);
        System.out.println("lnum="+lnum);
        System.out.println("c="+c);
        System.out.println("b="+b);
      // primitives are byte, short, long, int, float, double, boolean and char //
    }
}
