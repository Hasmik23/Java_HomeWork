package class7;

public class ForLoops5 {
    public static void main(String[] args) {
        //print table of 5

        for (int i = 1; i < 10; i++) {
            System.out.println("5 * " + i + " = " + (5 * i));
        }
// print table of 3
        for (int i = 1; i < 10; i++) {
            System.out.println("3 * " + i + " = " + (3 * i));

        }

        for (int i =1;i < 10;i++){
            i = i * 5;
            System.out.println("5 * " + i + " = " + i);
        }
    }
}


